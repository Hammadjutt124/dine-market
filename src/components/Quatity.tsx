"use client"
import React, { useState } from 'react'

const Quatity = () => {
    const [num, setNum] = useState(1);
    return (
        <div className='flex gap-x-2 '>
            {/*minus*/}
            <button className='border h-6 w-6 rounded-full center ' onClick={() => {
                setNum(num <= 1 ? 1 : num - 1);
            }}>
                -
            </button>
            {/*number*/}
            <div className='borde6 h-6 w-6 rounded-full center '>
                <span className='text-sm'>{num}</span>
            </div>
            {/*plus*/}
            <button className='border h-6 w-6 rounded-full center '
                onClick={() => {
                    setNum(num + 1);
                }}>
                +
            </button>
        </div>
    )
}

export default Quatity
